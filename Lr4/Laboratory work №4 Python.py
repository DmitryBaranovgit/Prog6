"""–í–∏–¥–Ω–æ, —á—Ç–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç–æ–ª–±—Ü–æ–≤ —É–º–µ–Ω—å—à–∏–ª–æ—Å—å –Ω–∞ –æ–¥–∏–Ω. –î–∞–Ω–Ω—ã–µ –≤ 5 –ø–µ—Ä–≤—ã—Ö —Å—Ç—Ä–æ–∫–∞—Ö —Ç–∞–∫–∏–µ –∂–µ, –∫–∞–∫ –±—ã–ª–∏ —Ä–∞–Ω–µ–µ.

# 5. –ü—Ä–æ–≤–∞–ª–∏–¥–∏—Ä–æ–≤–∞—Ç—å –º–æ–¥–µ–ª—å –Ω–∞ —Ç–µ—Å—Ç–æ–≤–æ–π –≤—ã–±–æ—Ä–∫–µ

**–®–∞–≥ 5.1. –°—Ä–∞–≤–Ω–µ–Ω–∏–µ –º–æ–¥–µ–ª–µ–π.**

–¢–µ–ø–µ—Ä—å –º—ã –≥–æ—Ç–æ–≤—ã —Å—Ä–∞–≤–Ω–∏—Ç—å –∫–∞—á–µ—Å—Ç–≤–æ –¥–≤—É—Ö –º–æ–¥–µ–ª–µ–π! üòé

*1. –ö–∞–∫–∞—è –º–æ–¥–µ–ª—å –ª—É—á—à–µ?*

–ü–æ–ª—É—á–∏–º –ø—Ä–æ–≥–Ω–æ–∑—ã —Ü–µ–ª–µ–≤–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –Ω–∞ —Ç–µ—Å—Ç–æ–≤—ã—Ö –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –º–æ–¥–µ–ª–∏ –ª–∏–Ω–µ–π–Ω–æ–π —Ä–µ–≥—Ä–µ—Å—Å–∏–∏ –º –º–æ–¥–µ–ª–∏ —Å–ª—É—á–∞–π–Ω–æ–≥–æ –ª–µ—Å–∞.

–î–ª—è —ç—Ç–æ–≥–æ –≤—ã–∑–æ–≤–µ–º —É –∫–∞–∂–¥–æ–π –º–æ–¥–µ–ª–∏ –º–µ—Ç–æ–¥ **predict()**, –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ –ø–µ—Ä–µ–¥–∞–¥–∏–º *test_points*.
"""

