"""–ö–∞—á–µ—Å—Ç–≤–æ —Ä–µ–≥—Ä–µ—Å—Å–∏–æ–Ω–Ω—ã—Ö –º–æ–¥–µ–ª–µ–π –æ—Ü–µ–Ω–∏–º –¥–≤—É–º—è —Å–ø–æ—Å–æ–±–∞–º–∏:
1. –°—Ä–∞–≤–Ω–∏–º –≤–∏–∑—É–∞–ª—å–Ω–æ –ø—Ä–æ–≥–Ω–æ–∑—ã —Å –Ω–∞—Å—Ç–æ—è—â–∏–º–∏ —Ü–µ–Ω–∞–º–∏ (—Ç–µ—Å—Ç–æ–≤—ã–µ —Å –ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏–µ–º)
2. –°—Ä–∞–≤–Ω–∏–º –º–µ—Ç—Ä–∏–∫–∏ –∫–∞—á–µ—Å—Ç–≤–∞

–í–∏–∑—É–∞–ª–∏–∑–∏—Ä—É–µ–º –ø—Ä–æ–≥–Ω–æ–∑ –ª–∏–Ω–µ–π–Ω–æ–π –º–æ–¥–µ–ª–∏ –∏ –Ω–∞—Å—Ç–æ—è—â–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è –∏–∑ —Ç–µ—Å—Ç–æ–≤–æ–π –≤—ã–±–æ—Ä–∫–∏.
–ú–æ–∂–Ω–æ –Ω–µ –æ–±—Ä–∞—â–∞—Ç—å –æ—Å–æ–±–æ–≥–æ –≤–Ω–∏–º–∞–Ω–∏—è –Ω–∞ —Ç–æ, —á—Ç–æ –∑–∞ –∫–æ–¥ –Ω–∞–ø–∏—Å–∞–Ω.
"""

# Commented out IPython magic to ensure Python compatibility.
# %matplotlib inline

plt.figure(figsize=(7, 7))
